package com.example.malpicasoft.proveedores;

class ProveedoresSetters {

    private String fechaAlta, horaAlta, fechaModif, horaModif, codigo, razonSocial, condicion,
            descripcion, cuit, direccion, localidad, contacto, tipo;

    String getFechaAlta() { return fechaAlta; }

    void setFechaAlta(String fechaAlta) { this.fechaAlta = fechaAlta; }

    String getHoraAlta() { return horaAlta; }

    void setHoraAlta(String horaAlta) { this.horaAlta = horaAlta; }

    String getFechaModif() { return fechaModif; }

    void setFechaModif(String fechaModif) { this.fechaModif = fechaModif; }

    String getHoraModif() { return horaModif; }

    void setHoraModif(String horaModif) { this.horaModif = horaModif; }

    String getCodigo() { return codigo; }

    void setCodigo(String codigo) { this.codigo = codigo; }

    String getRazonSocial() { return razonSocial; }

    void setRazonSocial(String razonSocial) { this.razonSocial = razonSocial; }

    String getCondicion() { return condicion; }

    void setCondicion(String condicion) { this.condicion = condicion; }

    String getDescripcion() { return descripcion; }

    void setDescripcion(String descripcion) { this.descripcion = descripcion; }

    String getCuit() { return cuit; }

    void setCuit(String cuit) { this.cuit = cuit; }

    String getDireccion() { return direccion; }

    void setDireccion(String direccion) { this.direccion = direccion; }

    String getLocalidad() { return localidad; }

    void setLocalidad(String localidad) { this.localidad = localidad; }

    String getContacto() { return contacto; }

    void setContacto(String contacto) { this.contacto = contacto; }

    String getTipo() { return tipo; }

    void setTipo(String tipo) { this.tipo = tipo; }
}
