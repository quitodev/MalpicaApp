package com.example.malpicasoft.usuario;

class UsuarioSetters {

    private String totalStock, totalVentas, totalCompras, totalGastos, estado, fechaAlta, local, direccion,
            localidad, cuit, condicion, email, telefono;

    String getTotalStock() { return totalStock; }

    void setTotalStock(String totalStock) { this.totalStock = totalStock; }

    String getTotalVentas() {
        return totalVentas;
    }

    void setTotalVentas(String totalVentas) {
        this.totalVentas = totalVentas;
    }

    String getTotalCompras() {
        return totalCompras;
    }

    void setTotalCompras(String totalCompras) {
        this.totalCompras = totalCompras;
    }

    String getTotalGastos() {
        return totalGastos;
    }

    void setTotalGastos(String totalGastos) {
        this.totalGastos = totalGastos;
    }

    String getLocal() {
        return local;
    }

    void setLocal(String local) {
        this.local = local;
    }

    String getDireccion() {
        return direccion;
    }

    void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    String getLocalidad() {
        return localidad;
    }

    void setLocalidad(String localidad) {
        this.localidad = localidad;
    }

    String getCuit() {
        return cuit;
    }

    void setCuit(String cuit) {
        this.cuit = cuit;
    }

    String getCondicion() {
        return condicion;
    }

    void setCondicion(String condicion) {
        this.condicion = condicion;
    }

    String getEmail() {
        return email;
    }

    void setEmail(String email) {
        this.email = email;
    }

    String getTelefono() {
        return telefono;
    }

    void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    String getEstado() {
        return estado;
    }

    void setEstado(String estado) {
        this.estado = estado;
    }

    String getFechaAlta() {
        return fechaAlta;
    }

    void setFechaAlta(String fechaAlta) {
        this.fechaAlta = fechaAlta;
    }
}
